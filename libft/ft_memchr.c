#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;
	char	*temp;

	i = 0;
	temp = (char *)s;
	while (i < n)
	{
		if (*temp == (char)c)
			return ((char *)temp);
		temp++;
		i++;
	}
	return (NULL);
}
